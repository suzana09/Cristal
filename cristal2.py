# LISTAS

lista_de_frutas= ['uva', 'banana','laranja']

#adicionando itens na lista - append()

lista_de_frutas.append('morango')
print(lista_de_frutas)


#insert()- adiciona um item em uma posição específica sem apagar o item q ja tava lá

lista_de_frutas.insert(1,'goiaba')
print(lista_de_frutas)


#del()- Exclui um item especi´fico de uma lista

del lista_de_frutas[0]
print(lista_de_frutas)


#pop()- Exclui o ultimo item da lista. Ou o item no index especifico
fruta_removida_da_lista= lista_de_frutas.pop(1)


#remove()- Remove um item especifico sem q seja necessario saber a posicao dele

